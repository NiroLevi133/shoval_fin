const RULES: { key: string; re: RegExp }[] = [
  { key: "dairy_protein", re: /יוגורט|גבינ|קוטג|לבנה|סימפוני|ריקוטה|בולגרית|צפתית|חמד|שמנת|מעדן|רוויון|חלב\b|ביצ|חביתה|שקשוק|טונה|סרדינ|פסטרמה|סלמון מעושן/ },
  { key: "meat_protein", re: /עוף|הודו|בקר|פרגית|שוקי|שווארמה|סטייק|קציצ|שניצל|המבורגר|נקניק|כבד|לבבות|טופו|סייטן|טבעול|פולי סויה|קטניות|\bדג\b|אמנון|לברק|פורל|סול|בקלה/ },
  { key: "carbs", re: /לחם|פיתה|אורז|פסטה|בורגול|קוסקוס|קינוא|פתית|בטטה|תפוח אדמה|תפו"א|פריכ|קרקר|דגני|טורטיי|לחמני|בייגל|קוואקר|גרנולה|פירה|קלח תירס|ערמונים|חיטה/ },
  { key: "fats", re: /שקד|אגוז|קשיו|בוטנ|פיסטוק|פקאן|לוז|ברזיל|זית|שמן|טחינה|חומוס|אבוקד|פסטו|סחוג|פשתן|צ'יה|שומשום|קוקוס|מיונז/ },
  { key: "fruits", re: /תפוח|אגס|אפרסק|תפוז|בננה|מנגו|מלון|אבטיח|תות|ענב|אשכולית|פומלה|קלמנט|קיווי|שזיף|תאנ|תמר|פירות יער|גויאבה|רימון|צימוק|דובדבן|ליצ|שסק|משמש|סברס|אננס/ },
  { key: "vegetables", re: /עגבני|מלפפון|פלפל|גזר|חסה|ברוקולי|כרוב|כרובית|קישוא|זוקיני|חציל|פטרי|תרד|דלעת|סלק|צנון|בצל|שומר|קולורבי|במיה|ארטישוק|סלרי|ירק|סלט/ },
];

export function detectGroupKey(text: string): string | null {
  for (const { key, re } of RULES) if (re.test(text)) return key;
  return null;
}

// פירוק תיאור ארוחה לרכיבים בודדים: "פריט + פריט + פריט" → [..]
export function parseComponents(description: string): string[] {
  return description
    .replace(/\(\d+\s*קק"ל\)/g, "")
    .split(/\s*\+\s*/)
    .map((s) => s.trim())
    .filter(Boolean);
}
